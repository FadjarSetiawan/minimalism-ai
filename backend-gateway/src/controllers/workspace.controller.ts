// 2. POST /api/workspace/write
export const writeWorkspaceFile = async (req: AuthRequest, res: Response) => {
  try {
    const { filePath, content } = req.body;
    if (!filePath) {
      return res.status(400).json({ error: 'File path is required.' });
    }

    const resolved = safeResolvePath(filePath);
    const parentDir = path.dirname(resolved);

    // Create directories if they do not exist
    if (!fs.existsSync(parentDir)) {
      fs.mkdirSync(parentDir, { recursive: true });
    }

    fs.writeFileSync(resolved, content || '');
    return res.json({ success: true, path: filePath });
  } catch (error: any) {
    console.error('Write File Error:', error);
    return res.status(500).json({ error: error.message || 'Failed to write file.' });
  }
};

// 3. POST /api/workspace/delete
export const deleteWorkspaceFile = async (req: AuthRequest, res: Response) => {
  try {
    const { filePath } = req.body;
    if (!filePath) {
      return res.status(400).json({ error: 'File path is required.' });
    }

    const resolved = safeResolvePath(filePath);
    if (!fs.existsSync(resolved)) {
      return res.status(404).json({ error: 'File or directory not found.' });
    }

    const stat = fs.statSync(resolved);
    if (stat.isDirectory()) {
      fs.rmSync(resolved, { recursive: true, force: true });
    } else {
      fs.unlinkSync(resolved);
    }

    return res.json({ success: true, path: filePath });
  } catch (error: any) {
    console.error('Delete File Error:', error);
    return res.status(500).json({ error: error.message || 'Failed to delete path.' });
  }
};

// 4. POST /api/workspace/execute
export const executeWorkspaceCommand = async (req: AuthRequest, res: Response) => {
  try {
    const { command } = req.body;
    if (!command) {
      return res.status(400).json({ error: 'Command is required.' });
    }

    // Restrict executing dangerous destructive commands
    const lower = command.toLowerCase().trim();
    if (lower.includes('rm -rf') || lower.includes('format') || lower.includes('mkfs') || lower.includes('shutdown')) {
      return res.status(400).json({ error: 'Command blocked: Unsafe shell execution.' });
    }

    exec(command, { cwd: SANDBOX_ROOT, timeout: 15000 }, (error, stdout, stderr) => {
      return res.json({
        success: !error,
        stdout: stdout || '',
        stderr: stderr || '',
        exitCode: error ? error.code : 0
      });
    });
  } catch (error: any) {
    console.error('Execute Command Error:', error);
    return res.status(500).json({ error: error.message || 'Failed to run command.' });
  }
};

// 4.1. GET /api/workspace/stream-execute (SSE Real shell stream compiler)
export const streamWorkspaceExecute = async (req: AuthRequest, res: Response) => {
  try {
    const command = req.query.command as string;
    if (!command) {
      return res.status(400).json({ error: 'Command is required.' });
    }

    // Restrict executing dangerous destructive commands
    const lower = command.toLowerCase().trim();
    if (lower.includes('rm -rf') || lower.includes('format') || lower.includes('mkfs') || lower.includes('shutdown')) {
      return res.status(400).json({ error: 'Command blocked: Unsafe shell execution.' });
    }

    // Set SSE headers
    res.writeHead(200, {
      'Content-Type': 'text/event-stream',
      'Cache-Control': 'no-cache',
      'Connection': 'keep-alive',
      'Access-Control-Allow-Origin': '*'
    });

    // Send initial status message
    res.write(`data: ${JSON.stringify({ type: 'stdout', text: `[SYSTEM] Spawning process: ${command}\n` })}\n\n`);

    // Kill existing active child process if running to prevent port lockups
    if (activeChildProcess) {
      try {
        res.write(`data: ${JSON.stringify({ type: 'stdout', text: `[SYSTEM] Terminating active running process to prevent collision...\n` })}\n\n`);
        activeChildProcess.kill('SIGINT');
      } catch (e) {}
    }

    // Spawn the shell process natively inside the sandboxed workspace folder
    activeChildProcess = spawn(command, { shell: true, cwd: SANDBOX_ROOT });

    activeChildProcess.stdout.on('data', (chunk: any) => {
      res.write(`data: ${JSON.stringify({ type: 'stdout', text: chunk.toString() })}\n\n`);
    });

    activeChildProcess.stderr.on('data', (chunk: any) => {
      res.write(`data: ${JSON.stringify({ type: 'stderr', text: chunk.toString() })}\n\n`);
    });

    activeChildProcess.on('error', (err: any) => {
      res.write(`data: ${JSON.stringify({ type: 'error', text: err.message })}\n\n`);
      res.end();
      activeChildProcess = null;
    });

    activeChildProcess.on('close', (code: number) => {
      res.write(`data: ${JSON.stringify({ type: 'exit', code })}\n\n`);
      res.end();
      activeChildProcess = null;
    });

  } catch (error: any) {
    console.error('SSE Spawn Error:', error);
    try {
      res.write(`data: ${JSON.stringify({ type: 'error', text: error.message || 'Stream initiation failed.' })}\n\n`);
      res.end();
    } catch (e) {}
  }
};

// Flatten recursive file structure for LLM context injection
function flattenFilesForContext(files: WorkspaceFile[]): string {
  let context = '';
  function traverse(item: WorkspaceFile) {
    if (item.type === 'file') {
      context += `\n[FILE: ${item.path}]\n${item.content || ''}\n[END_FILE]\n`;
    } else if (item.children) {
      for (const child of item.children) {
        traverse(child);
      }
    }
  }

  for (const item of files) {
    traverse(item);
  }
  return context;
}

