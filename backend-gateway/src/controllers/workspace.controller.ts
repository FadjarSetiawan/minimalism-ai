// Clear all generated files from the sandbox (keeps directory structure, wipes src/ and generated files)
function clearSandboxWorkspace() {
  const clearDir = (dir: string) => {
    if (!fs.existsSync(dir)) return;
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const fullPath = path.join(dir, entry.name);
      // Never delete node_modules or .git
      if (entry.name === 'node_modules' || entry.name === '.git') continue;
      if (entry.isDirectory()) {
        clearDir(fullPath);
        try { fs.rmdirSync(fullPath); } catch (e) {} // remove if empty
      } else {
        // Keep package.json and README.md
        if (entry.name === 'package.json' || entry.name === 'README.md') continue;
        fs.unlinkSync(fullPath);
      }
    }
  };
  clearDir(SANDBOX_ROOT);
  // Re-scaffold the src directory so the server still has a valid structure
  const srcDir = path.join(SANDBOX_ROOT, 'src');
  if (!fs.existsSync(srcDir)) {
    fs.mkdirSync(srcDir, { recursive: true });
  }
}


// Safely verify and resolve file paths inside sandbox to avoid directory traversal
function safeResolvePath(relativePath: string): string {
  const resolved = path.resolve(SANDBOX_ROOT, relativePath);
  if (!resolved.startsWith(SANDBOX_ROOT)) {
    throw new Error('Access Denied: Path escapes sandbox boundary.');
  }
  return resolved;
}

// Recursively fetch files inside the sandbox
interface WorkspaceFile {
  path: string;
  name: string;
  type: 'file' | 'directory';
  content?: string;
  children?: WorkspaceFile[];
}

function scanDirectory(dir: string, relativeRoot = ''): WorkspaceFile[] {
  const items = fs.readdirSync(dir, { withFileTypes: true });
  const results: WorkspaceFile[] = [];

  for (const item of items) {
    const relativePath = path.join(relativeRoot, item.name).replace(/\\/g, '/');
    const absolutePath = path.join(dir, item.name);

    if (item.isDirectory()) {
      // Exclude node_modules, .git, dist for performance
      if (item.name === 'node_modules' || item.name === '.git' || item.name === 'dist') {
        continue;
      }
      results.push({
        path: relativePath,
        name: item.name,
        type: 'directory',
        children: scanDirectory(absolutePath, relativePath)
      });
    } else {
      // Only read files that are common text types to prevent binary pollution
      const ext = path.extname(item.name).toLowerCase();
      const textExtensions = ['.html', '.css', '.js', '.ts', '.json', '.md', '.txt', '.prisma', '.env'];
      let content = '';
      if (textExtensions.includes(ext)) {
        try {
          content = fs.readFileSync(absolutePath, 'utf-8');
        } catch (e) {
          content = '[Failed to read file contents]';
        }
      } else {
        content = '[Binary file binary data not loaded]';
      }

      results.push({
        path: relativePath,
        name: item.name,
        type: 'file',
        content
      });
    }
  }

  // Sort directories first, then files alphabetically
  return results.sort((a, b) => {
    if (a.type !== b.type) {
      return a.type === 'directory' ? -1 : 1;
    }
    return a.name.localeCompare(b.name);
  });
}

